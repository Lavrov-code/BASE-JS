// Функции для арифметических операций
// // 5. Реализовать функцию с тремя параметрами: function mathOperation(arg1, arg2, operation), 
// где arg1, arg2 – значения аргументов, operation – строка с названием операции. В зависимости от 
// переданного значения операции (использовать switch) выполнить одну из арифметических 
// операций 
// (использовать функции из задания 4) и вернуть полученное значение. 
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    if (b === 0) {
        throw new Error("Деление на ноль невозможно.");
    }
    return a / b;
}

// Функция для выполнения арифметической операции в зависимости от переданного аргумента
function mathOperation(arg1, arg2, operation) {
    switch (operation) {
        case 'add':
            return add(arg1, arg2);
        case 'subtract':
            return subtract(arg1, arg2);
        case 'multiply':
            return multiply(arg1, arg2);
        case 'divide':
            return divide(arg1, arg2);
        default:
            throw new Error("Неизвестная операция: " + operation);
    }
}

// Примеры использования функций
console.log(mathOperation(5, 3, 'add'));       // 8
console.log(mathOperation(5, 3, 'subtract'));  // 2
console.log(mathOperation(5, 3, 'multiply'));  // 15
console.log(mathOperation(5, 3, 'divide'));    // 1.6666666666666667

